function [ Phi ] = FuncoesForma_Quad8( e1,e2 )

Phi(1) = - 1/4 * ( 1 - e1) * (1 + e2) * (1 + e1 - e2 ) ;
Phi(2) = - 1/4 * ( 1 + e1) * (1 + e2) * (1 - e1 - e2 ) ;
Phi(3) = + 1/4 * ( 1 + e1) * (1 - e2) * (-1 + e1 - e2 ) ;
Phi(4) = + 1/4 * ( 1 - e1) * (1 - e2) * (-1 - e1 - e2 ) ;

Phi(5) = 1/2 *  ( 1 + e1 ) * ( 1 - e1) * ( 1 + e2);
Phi(6) = 1/2 *  ( 1 + e1 ) * ( 1 + e2) * ( 1 - e2);
Phi(7) = 1/2 *  ( 1 - e1 ) * ( 1 + e1) * ( 1 - e2);
Phi(8) = 1/2 *  ( 1 - e1 ) * ( 1 - e2) * ( 1 + e2);


end

