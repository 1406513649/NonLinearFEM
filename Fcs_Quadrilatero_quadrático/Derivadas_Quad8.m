function Psi = Derivadas_Quad8 ( e1,e2 )


	% Derivada em e1
    
    Psi(1,1) = - 1/4 * ( 1 + e2 ) * (-2*e1 + e2) ;
    Psi(2,1) = - 1/4 * ( 1 + e2 ) * (-2*e1 - e2) ;
    Psi(3,1) = - 1/4 * ( 1 - e2 ) * (+2*e1 - e2) ;
    Psi(4,1) = - 1/4 * ( 1 - e2 ) * (+2*e1 + e2) ;

    Psi(5,1) = 1 / 2 * (1 + e2) * ( - 2* e2 ) ;
    Psi(6,1) = 1 / 2 * (1 - e2^2) ;
    Psi(7,1) = 1 / 2 * (1 - e2 ) * ( -2 *e1) ;
    Psi(8,1) = 1 / 2 * (1 - e2 ^2) * ( -1 ) ;
    
    % Derivada em e2
  
    Psi(1,2) = - 1/4 * ( 1 - e1 ) * (-2*e2 + e1) ;
    Psi(2,2) = - 1/4 * ( 1 + e1 ) * (-2*e2 - e1) ;
    Psi(3,2) = + 1/4 * ( 1 + e1 ) * (+2*e2 - e1) ;
    Psi(4,2) = + 1/4 * ( 1 + e1 ) * (+2*e2 + e1) ;

    Psi(5,2) = 1 / 2 * (1 - e1 ^ 2)  ;
    Psi(6,2) = 1 / 2 * (1 + e1 ) * ( -2*e2);
    Psi(7,2) = 1 / 2 * (1 - e1 ^2) * (-1) ;
    Psi(8,2) = 1 / 2 * (1 - e1 ) * ( -2*e2);
    
end 